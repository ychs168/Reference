package com.ychs.java;

/**
 * 完成两个double型数字的计算
 * 
 * @author wuzhijun
 * @version 1.0  
 */
public class Calc {
	/**
	 * 完成加法计算
	 * 
	 * @param a
	 *            第一个操作数
	 * @param b
	 *            第二个操作数
	 * @return 加法计算得到的结果
	 */
	public double add(int a, int b) {
		return a + b;
	}

	/**
	 * 完成减法计算
	 * 
	 * @param a
	 *            第一个操作数
	 * @param b
	 *            第二个操作数
	 * @return 减法计算得到的结果
	 */
	public double sub(int a, int b) {
		return a - b;
	}

	/**
	 * 完成乘法计算
	 * 
	 * @param a
	 *            第一个操作数
	 * @param b
	 *            第二个操作数
	 * @return 乘法计算得到的结果
	 */
	public double mul(int a, int b) {
		return a * b;
	}

	/**
	 * 完成除法计算
	 * 
	 * @param a
	 *            第一个操作数
	 * @param b
	 *            第二个操作数
	 * @return 除法计算得到的结果
	 */
	public double div(int a, int b) {
		return a / b;
	}

}
